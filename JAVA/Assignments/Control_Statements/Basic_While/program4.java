



		 // WAP to print square of first 10 natural numbers in reverse manner.



                class ReverseSquare{

                        public static void main(String[]args){

                                int num = 10;

					while(num >=1){

                                                 System.out.print((num*num) + " ");
                                
                                                 num --;
				}
                                                 System.out.println();
                        }
                }
