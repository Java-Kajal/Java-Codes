



		// WAP to print count down of days to submit the assignment.



                class CountDown{

                        public static void main(String[]args){

                                int day = 7;

                                        while(day >=0){

						if(day >= 1){

                                                 System.out.println(day + " days remaining ");
					}else{
						System.out.println(day + " days Assignment is Overdue");
					}
                                                 day --;
                                }
						System.out.println();
                                         
			}
                }
